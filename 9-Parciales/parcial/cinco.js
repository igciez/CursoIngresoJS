function Mostrar()
{
	var dia;

	dia=prompt("Ingrese un dia de la semana");

	switch (dia) {

		case "Lunes":{
			alert("Lunes: a trabajar !!!");break;
		}
		case "Martes":{
			alert("Martes: a trabajar !!!");break;
		}
		case "Miercoles":{
			alert("Miercoles: a trabajar !!!");break;
		}
		case "Jueves":{
			alert("Jueves: a trabajar !!!");break;
		}
		case "Viernes":{
			alert("Viernes: a trabajar !!!");break;
		}
		case "Sabado":{
			alert("Sabado: es fin de semana");break;
		}
		case "Domingo":{
			alert("Domingo: es fin de semana");break;
		}

	}
		

}
